pub fn sm3p0(x: u64) -> u64 {
    let x = x as u32;
    sext32(x ^ rol32(x, 9) ^ rol32(x, 17))
}
pub fn sm3p1(x: u64) -> u64 {
    let x = x as u32;
    sext32(x ^ rol32(x, 15) ^ rol32(x, 23))
}

