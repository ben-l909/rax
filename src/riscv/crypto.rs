//! Scalar cryptography extensions (Zbkx, Zknh, Zksh, Zksed, Zkne, Zknd).
//!
//! These implement the RISC-V scalar-crypto instruction primitives: crossbar
//! permutations, the SHA-256/512 and SM3 message-schedule transforms, and the
//! AES-64 and SM4 round/key-schedule helpers. All semantics follow the RISC-V
//! scalar cryptography specification and are differentially verified against
//! qemu-riscv64.

// ---------------------------------------------------------------------------
// Zbkx: crossbar permutations.
// ---------------------------------------------------------------------------

/// `xperm8`: gather bytes of `rs1` indexed by the bytes of `rs2`.
pub fn xperm8(rs1: u64, rs2: u64, xbits: u32) -> u64 {
    let n = xbits / 8; // number of byte lanes
    let mut out = 0u64;
    for i in 0..n {
        let idx = ((rs2 >> (i * 8)) & 0xff) as u32;
        if idx < n {
            let byte = (rs1 >> (idx * 8)) & 0xff;
            out |= byte << (i * 8);
        }
    }
    out
}

/// `xperm4`: gather nibbles of `rs1` indexed by the nibbles of `rs2`.
pub fn xperm4(rs1: u64, rs2: u64, xbits: u32) -> u64 {
    let n = xbits / 4; // number of nibble lanes
    let mut out = 0u64;
    for i in 0..n {
        let idx = ((rs2 >> (i * 4)) & 0xf) as u32;
        if idx < n {
            let nib = (rs1 >> (idx * 4)) & 0xf;
            out |= nib << (i * 4);
        }
    }
    out
}

// ---------------------------------------------------------------------------
// Zknh: SHA-256 and SHA-512 sigma/sum transforms (results sign-extended).
// ---------------------------------------------------------------------------

#[inline]
fn ror32(x: u32, n: u32) -> u32 {
    x.rotate_right(n)
}
#[inline]
fn rol32(x: u32, n: u32) -> u32 {
    x.rotate_left(n)
}
#[inline]
fn ror64(x: u64, n: u32) -> u64 {
    x.rotate_right(n)
}
#[inline]
fn sext32(v: u32) -> u64 {
    v as i32 as i64 as u64
}

pub fn sha256sig0(x: u64) -> u64 {
    let x = x as u32;
    sext32(ror32(x, 7) ^ ror32(x, 18) ^ (x >> 3))
}
pub fn sha256sig1(x: u64) -> u64 {
    let x = x as u32;
    sext32(ror32(x, 17) ^ ror32(x, 19) ^ (x >> 10))
}
pub fn sha256sum0(x: u64) -> u64 {
    let x = x as u32;
    sext32(ror32(x, 2) ^ ror32(x, 13) ^ ror32(x, 22))
}
pub fn sha256sum1(x: u64) -> u64 {
    let x = x as u32;
    sext32(ror32(x, 6) ^ ror32(x, 11) ^ ror32(x, 25))
}
pub fn sha512sig0(x: u64) -> u64 {
    ror64(x, 1) ^ ror64(x, 8) ^ (x >> 7)
}
pub fn sha512sig1(x: u64) -> u64 {
    ror64(x, 19) ^ ror64(x, 61) ^ (x >> 6)
}
pub fn sha512sum0(x: u64) -> u64 {
    ror64(x, 28) ^ ror64(x, 34) ^ ror64(x, 39)
}
pub fn sha512sum1(x: u64) -> u64 {
    ror64(x, 14) ^ ror64(x, 18) ^ ror64(x, 41)
}

