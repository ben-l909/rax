/// GF(2^8) multiply by `x` (the AES field, reduction polynomial 0x11b).
#[inline]
fn xtime(a: u8) -> u8 {
    let h = (a >> 7) & 1;
    ((a << 1) ^ (h * 0x1b)) & 0xff
}
/// GF(2^8) multiply.
fn gfmul(mut a: u8, mut b: u8) -> u8 {
    let mut p = 0u8;
    for _ in 0..8 {
        if b & 1 != 0 {
            p ^= a;
        }
        let hi = a & 0x80;
        a <<= 1;
        if hi != 0 {
            a ^= 0x1b;
        }
        b >>= 1;
    }
    p
}

fn subbytes_fwd(x: u64) -> u64 {
    let mut out = 0u64;
    for i in 0..8 {
        let b = ((x >> (i * 8)) & 0xff) as u8;
        out |= (AES_SBOX[b as usize] as u64) << (i * 8);
    }
    out
}
fn subbytes_inv(x: u64) -> u64 {
    let mut out = 0u64;
    for i in 0..8 {
        let b = ((x >> (i * 8)) & 0xff) as u8;
        out |= (AES_INV_SBOX[b as usize] as u64) << (i * 8);
    }
    out
}

/// AES forward MixColumns on a single 32-bit column.
fn mixcol_fwd(col: u32) -> u32 {
    let b = col.to_le_bytes();
    let mut o = [0u8; 4];
    o[0] = xtime(b[0]) ^ (xtime(b[1]) ^ b[1]) ^ b[2] ^ b[3];
    o[1] = b[0] ^ xtime(b[1]) ^ (xtime(b[2]) ^ b[2]) ^ b[3];
    o[2] = b[0] ^ b[1] ^ xtime(b[2]) ^ (xtime(b[3]) ^ b[3]);
    o[3] = (xtime(b[0]) ^ b[0]) ^ b[1] ^ b[2] ^ xtime(b[3]);
    u32::from_le_bytes(o)
}
/// AES inverse MixColumns on a single 32-bit column.
fn mixcol_inv(col: u32) -> u32 {
    let b = col.to_le_bytes();
    let mut o = [0u8; 4];
    o[0] = gfmul(b[0], 14) ^ gfmul(b[1], 11) ^ gfmul(b[2], 13) ^ gfmul(b[3], 9);
    o[1] = gfmul(b[0], 9) ^ gfmul(b[1], 14) ^ gfmul(b[2], 11) ^ gfmul(b[3], 13);
    o[2] = gfmul(b[0], 13) ^ gfmul(b[1], 9) ^ gfmul(b[2], 14) ^ gfmul(b[3], 11);
    o[3] = gfmul(b[0], 11) ^ gfmul(b[1], 13) ^ gfmul(b[2], 9) ^ gfmul(b[3], 14);
    u32::from_le_bytes(o)
}

fn mixcols_fwd(x: u64) -> u64 {
    let lo = mixcol_fwd(x as u32);
    let hi = mixcol_fwd((x >> 32) as u32);
    (lo as u64) | ((hi as u64) << 32)
}
fn mixcols_inv(x: u64) -> u64 {
    let lo = mixcol_inv(x as u32);
    let hi = mixcol_inv((x >> 32) as u32);
    (lo as u64) | ((hi as u64) << 32)
}

/// ShiftRows (forward) on the 128-bit state {rs2:rs1}, low 64 bits.
fn shiftrows_fwd(rs1: u64, rs2: u64) -> u64 {
    let b = |v: u64, i: u32| (v >> (i * 8)) & 0xff;
    // bytes [rs1.0, rs1.5, rs2.2, rs2.7, rs1.4, rs2.1, rs2.6, rs1.3]
    b(rs1, 0)
        | (b(rs1, 5) << 8)
        | (b(rs2, 2) << 16)
        | (b(rs2, 7) << 24)
        | (b(rs1, 4) << 32)
        | (b(rs2, 1) << 40)
        | (b(rs2, 6) << 48)
        | (b(rs1, 3) << 56)
}
/// ShiftRows (inverse) on the 128-bit state {rs2:rs1}, low 64 bits.
fn shiftrows_inv(rs1: u64, rs2: u64) -> u64 {
    let b = |v: u64, i: u32| (v >> (i * 8)) & 0xff;
    // bytes [rs1.0, rs2.5, rs2.2, rs1.7, rs1.4, rs1.1, rs2.6, rs2.3]
    b(rs1, 0)
        | (b(rs2, 5) << 8)
        | (b(rs2, 2) << 16)
        | (b(rs1, 7) << 24)
        | (b(rs1, 4) << 32)
        | (b(rs1, 1) << 40)
        | (b(rs2, 6) << 48)
        | (b(rs2, 3) << 56)
}

pub fn aes64es(rs1: u64, rs2: u64) -> u64 {
    subbytes_fwd(shiftrows_fwd(rs1, rs2))
}
pub fn aes64esm(rs1: u64, rs2: u64) -> u64 {
    mixcols_fwd(subbytes_fwd(shiftrows_fwd(rs1, rs2)))
}
pub fn aes64ds(rs1: u64, rs2: u64) -> u64 {
    subbytes_inv(shiftrows_inv(rs1, rs2))
}
pub fn aes64dsm(rs1: u64, rs2: u64) -> u64 {
    mixcols_inv(subbytes_inv(shiftrows_inv(rs1, rs2)))
}

/// `aes64im`: inverse MixColumns of `rs1` (for the decryption key schedule).
pub fn aes64im(rs1: u64) -> u64 {
    mixcols_inv(rs1)
}

const AES_RCON: [u8; 10] = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36];

/// `aes64ks1i`: AES key-schedule step 1 with round number `rnum` (0..=10).
pub fn aes64ks1i(rs1: u64, rnum: u32) -> u64 {
    let tmp1 = (rs1 >> 32) as u32; // high 32 bits
    let (tmp2, rc) = if rnum == 0xA {
        (tmp1, 0u32)
    } else {
        (tmp1.rotate_right(8), AES_RCON[rnum as usize] as u32)
    };
    let tmp3 = subbytes_fwd(tmp2 as u64) as u32; // sbox each byte (low 32)
    let result = tmp3 ^ rc;
    (result as u64) | ((result as u64) << 32)
}

/// `aes64ks2`: AES key-schedule step 2.
pub fn aes64ks2(rs1: u64, rs2: u64) -> u64 {
    let w0 = (rs1 >> 32) as u32 ^ rs2 as u32;
    let w1 = w0 ^ (rs2 >> 32) as u32;
    (w0 as u64) | ((w1 as u64) << 32)
}

